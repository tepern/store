     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $fields = [
            'id',
            'name',
            'price',
            'category_id'
        ];

        $paginator = Product::select($fields)
            ->orderBy('id', 'DESC')
            ->with([
                'category' => function($query) {
                    $query->select(['id', 'name']);
                }])
            ->paginate(25);

        return view('products.index', compact('paginator'));
    }

